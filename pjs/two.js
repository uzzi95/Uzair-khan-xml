let q = 70;
q+=10; /// q = 70=10 =>80
console.log(q);
// Post Increment 
console.log(q+1);  // q = q+1 70 +1 =>71
console.log(q); // 71

// Pre Increment 
console.log(++q);  // 71 +1

let p = 70;
p-=10; /// q = 70=10 =<80
console.log(p);
// Post Decrement 
console.log(p-1); // q = q-1 70 -1 =>69
console.log(p); // 69

// Pre Decrement 
console.log(--p); // 70 -1